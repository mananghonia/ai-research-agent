"""
Evals — LLM-as-judge evaluation of generated research reports.

Uses Claude to score each report on 4 dimensions (0-10 each):
  - Accuracy:    Is it grounded in the sources? No hallucinations?
  - Completeness: Did it cover the topic thoroughly?
  - Clarity:     Is it well-structured and easy to read?
  - Source Quality: Are the sources credible and relevant?
"""

import json
import anthropic
from decouple import config

_client = None


def get_client() -> anthropic.Anthropic:
    global _client
    if _client is None:
        _client = anthropic.Anthropic(api_key=config('ANTHROPIC_API_KEY'))
    return _client


EVAL_PROMPT = """You are a research quality evaluator. Score this AI-generated research report on 4 dimensions.

Context: The report was generated by an AI agent that searched the web and synthesised findings from online sources (news, blogs, forums, company pages). It is NOT expected to cite academic papers — web sources are appropriate and normal.

## Report Topic
{topic}

## Report Content
{content}

## Sources Used
{sources}

## Scoring Guide (calibrated for AI web-search research)

**Accuracy** — Does the report accurately represent the sources? Is information grounded and internally consistent?
- 9-10: Every claim is well-supported, no contradictions
- 7-8:  Most claims supported; minor gaps or ambiguities
- 5-6:  Some unsupported assertions or inconsistencies
- 3-4:  Significant inaccuracies or hallucinations
- 0-2:  Mostly fabricated or wrong

**Completeness** — Does it cover the topic's main angles, sub-questions, and context?
- 9-10: Comprehensive — covers all major angles with depth
- 7-8:  Covers the key aspects well; a few minor gaps
- 5-6:  Covers the basics but misses important dimensions
- 3-4:  Superficial; significant gaps
- 0-2:  Barely touches the topic

**Clarity** — Is the report well-structured, readable, and easy to follow?
- 9-10: Excellent structure, clear headings, flows naturally
- 7-8:  Well-organised and readable; minor formatting issues
- 5-6:  Acceptable structure but some confusing sections
- 3-4:  Poorly organised or hard to follow
- 0-2:  Incoherent

**Source Quality** — Are the sources relevant and credible for this topic?
- 9-10: All sources directly relevant and from authoritative sites
- 7-8:  Most sources relevant and credible (news, reputable blogs, official sites)
- 5-6:  Mix of good and mediocre sources; some tangential
- 3-4:  Mostly low-quality or off-topic sources
- 0-2:  No useful sources

## Instructions
A well-researched AI report that covers the topic, cites real sources, and is clearly written should score 7-8 across dimensions. Reserve scores below 5 for genuine failures (wrong facts, missing topic, unreadable). Reserve 9-10 for exceptional depth.

Respond ONLY with valid JSON in this exact format:
{{
  "accuracy": {{
    "score": <integer 0-10>,
    "reason": "<one sentence explaining the score>"
  }},
  "completeness": {{
    "score": <integer 0-10>,
    "reason": "<one sentence explaining the score>"
  }},
  "clarity": {{
    "score": <integer 0-10>,
    "reason": "<one sentence explaining the score>"
  }},
  "source_quality": {{
    "score": <integer 0-10>,
    "reason": "<one sentence explaining the score>"
  }}
}}"""


def run_eval(topic: str, content: str, sources: list) -> dict | None:
    """
    Run LLM-as-judge evaluation on a research report.
    Returns a dict with scores and reasons, or None if eval fails.
    """
    sources_text = "\n".join(
        f"- {s.get('title', 'Untitled')} ({s.get('url', '')})"
        for s in sources[:10]
    ) if sources else "No sources provided."

    prompt = EVAL_PROMPT.format(
        topic=topic,
        content=content[:12000],  # Haiku has 200k context; 12k lets judge see full report
        sources=sources_text,
    )

    try:
        response = get_client().messages.create(
            model="claude-haiku-4-5-20251001",  # use Haiku — faster + cheaper for evals
            max_tokens=512,
            messages=[{"role": "user", "content": prompt}],
        )
        raw = response.content[0].text.strip()

        # Strip markdown code fences if Claude wraps the JSON
        if raw.startswith("```"):
            raw = raw.split("```")[1]
            if raw.startswith("json"):
                raw = raw[4:]

        scores = json.loads(raw)

        # Validate structure
        required = {"accuracy", "completeness", "clarity", "source_quality"}
        if not required.issubset(scores.keys()):
            return None

        # Clamp all scores to 0-10
        for key in required:
            scores[key]["score"] = max(0, min(10, int(scores[key]["score"])))

        # Add overall average
        avg = sum(scores[k]["score"] for k in required) / len(required)
        scores["overall"] = round(avg, 1)

        return scores

    except Exception:
        return None
